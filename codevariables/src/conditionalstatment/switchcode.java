package conditionalstatment;

public class switchcode {
    public static void main(String[] args) {
        int no=2022;
        switch (no){
            case 1:
                System.out.println("yes it is 1");break;
            case 500:
                System.out.println( "yes  it is 500");
                break;
            case 2000:
                System.out.println( "yes  it is 2000");break;
            case 2022:
                System.out.println( "yes  it is 2020");break;
            case 5000:
                System.out.println( "yes  it is 5000");break;
            default:
                System.out.println( "yes  it is 50000");
        }
    }
}
